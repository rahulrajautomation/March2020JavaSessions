package JavaSessions;


public class DataTypes {

	public static void main(String[] args) {
	byte b=10; // size- 1 Byte
	byte b1=-1;
	System.out.println(b+b1);
	
	short s=255; //size 2 bytes= 16 bits
	
	int i=10000;// size 4 bytes=32 bits
	
	long l=121212121;// size=8 byte= 64 bits
	
	float f1=12.33f; // size 4 byte(32 bits)
	
	float f2=100; // in float we can store int nut in int we cant store float.
	System.out.println(f1+f2);
	
	double d1=91.098765;// size- 8 bytes range - upto 16 decimal digits
	
	char c='A';
	char c1='1';
	System.out.println(c+c1);
	
		
	}

}
